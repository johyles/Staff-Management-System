package com.gy.entity;

public class Dept {

    private int deptid;
    private String name;
    private String addr ;

    public int getDeptid() {
        return deptid;
    }

    public void setdeptid(int deptid) {
        this.deptid = deptid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }
}
